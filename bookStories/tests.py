import unittest

from parameterized import parameterized

from bookStories.book_stories import stories


class Tests(unittest.TestCase):

    @parameterized.expand([
        [[6, 15, 21, 30], [
            [3, 7, 8],
            [9, 4, 2],
        ], 1, 6],
        [[6, 15, 21, 30], [
            [3, 7, 8],
            [9, 4, 2],
        ], 2, -1],
        [[6, 15, 21, 30], [[3, 14, 2]], 1, 15],
        [[6, 15, 21, 30], [[3, 14, 2]], 2, -1],
        [[6, 15, 21, 30], [
            [5, 11, 28],
            [9, 19, 29],
            [14, 16, 20],
            [18, 7, 22],
            [25, 6, 30],
        ], 1, 21],
        [[6, 15, 21, 30], [
            [5, 11, 28],
            [9, 19, 29],
            [14, 16, 20],
            [18, 7, 22],
            [25, 6, 30],
        ], 2, 30],
        [[6, 15, 21, 30], [
            [2, 10, 15],
            [3, 4, 10],
            [4, 3, 15],
            [10, 3, 15],
        ], 1, -1],
        [[6, 15, 21, 30], [
            [2, 10, 15],
            [3, 4, 10],
            [4, 3, 15],
            [10, 3, 15],
        ], 2, 15],
        [[11], [
            [2, 3, 4],
            [5, 10, 2],
        ], 1, 11],
        [[11], [
            [2, 3, 4],
            [5, 10, 2],
        ], 2, -1],
        [[11], [], 1, 11],
        [[11], [], 2, 11],
        [[4, 11], [
            [10, 6, 8],
        ], 1, 4],
        [[4, 11], [
            [10, 6, 8],
        ], 2, 4],
    ])
    def test_solution(self, endings, choices, option, expected_result):
        self.assertEqual(expected_result, stories(endings, choices, option))
